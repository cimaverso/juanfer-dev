export function getPolizaById(id) {
  return MOCK_POLIZAS.find((p) => p.id === parseInt(id)) || null
}

export function getPolizasByResponsable(responsable_id) {
  return MOCK_POLIZAS.filter((p) => p.responsable_id === responsable_id)
}

export function getPolizasByEstado(estado) {
  return MOCK_POLIZAS.filter((p) => p.estado === estado)
}

export function getPolizasByCliente(cliente_id) {
  return MOCK_POLIZAS.filter((p) => p.cliente_id === cliente_id)
}